public class javaLiteral {
    public static void main(String[] args) {
        byte b = 10;
        byte b1 = 0b1010;
        byte b2 = 012;
        byte b3 = 0xA;

        System.out.println(b);
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
    }
}
